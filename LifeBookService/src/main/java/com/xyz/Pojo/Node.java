package com.xyz.Pojo;

public class Node {

    public String name;
    public String child;

    public void setName(String name) {
        this.name = name;
    }

    public String getChild() {
        return child;
    }

    public void setChild(String child) {
        this.child = child;
    }

    public String getName() {
        return name;
    }
}
